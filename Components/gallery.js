let galleryObserverOptions = {
    root: null,
    rootMargin: '0px',
    threshold: 0.2
}
let galleryObserverCallback = (entries, observer) => {
    entries.forEach(entry => {
        // console.log(entry)
        if (entry.target.id == 'pageEnd') {
            if (entry.isIntersecting && !appending) {
                if (galleryId) {
                    setGalleryImages(galleryId);
                } else {
                    setGalleryImages(false, galleryQuery);
                }
            }
        }
    });
}
let galleryObserver = new IntersectionObserver(galleryObserverCallback, galleryObserverOptions);
let galleryTarget = document.getElementById('pageEnd');
galleryObserver.observe(galleryTarget);





